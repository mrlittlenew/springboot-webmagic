CREATE TABLE
    T_JING_DONG_PRODUCT
    (
        SKU BIGINT NOT NULL,
		NAME VARCHAR(255),
        CAT_ID VARCHAR(255),
        CAT_NAME VARCHAR(255),
        DESCRIPTION VARCHAR(255),
        SELLER VARCHAR(255),
        SHOP_ID VARCHAR(255),
        SHOP_NAME VARCHAR(255),
        VENDER_ID VARCHAR(255),
		LAST_UPDATE_DATE DATETIME,
        PRIMARY KEY (SKU)
    )
    ENGINE=INNODB DEFAULT CHARSET=UTF8;
	
CREATE TABLE
    T_JING_DONG_PRODUCT_HTML
    (
        SKU BIGINT NOT NULL,
		HTML LONGTEXT,
		LAST_UPDATE_DATE DATETIME,
        PRIMARY KEY (SKU)
    )
    ENGINE=INNODB DEFAULT CHARSET=UTF8;
	
CREATE TABLE
    T_JING_DONG_PRICE
    (
        ID BIGINT NOT NULL AUTO_INCREMENT,
		SKU BIGINT,
		PRICE DOUBLE,
        LAST_UPDATE_DATE DATETIME,
        PRIMARY KEY (ID)
    )
    ENGINE=INNODB DEFAULT CHARSET=UTF8;